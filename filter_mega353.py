# Define functions:


def createfolder(directory):
    """Attempts to create a directory named after the name provided, and provides an error message on failure"""
    try:
        if not os.path.exists(directory):
            os.makedirs(directory)
    except OSError:
        logger.info(f'Error: Creating directory: {directory}')


def file_exists_and_not_empty(file_name):
    """Check if file exists and is not empty by confirming that its size is not 0 bytes"""
    # Check if file exist and is not empty
    return os.path.isfile(file_name) and not os.path.getsize(file_name) == 0


def create_control_dataframe():
    """Returns a pandas dataframe for filtering."""
    control_df = pd.DataFrame(list(itertools.zip_longest(Target_name, Group, Order, Family, Species, three5three_list)),
                              columns=['Target_name', 'Group', 'Order', 'Family', 'Species', 'three5three_list'])
    return control_df


def write_filtering_options_dataframe():
    """Writes a .csv file with groups for filtering."""
    filtering_options_df = pd.DataFrame(list(itertools.zip_longest(Target_name, Group, Order, Family, Species)),
                                        columns=['Target_name', 'Group', 'Order', 'Family', 'Species'])
    filtering_options_df.to_csv('filtering_options.csv', index=False)


def parse_select_file(control_df, select_file):
    """Parse the select file and control file, and return a list of selected taxa (four-letter codes) and a list of
    original 353 target sequence names"""
    select_list = set()
    with open(select_file) as select:
        lines = select.readlines()
    for line in lines:
        for ind in control_df.index:
            if line.strip() in set(control_df.iloc[ind, 0:5]):
                select_list.add(control_df.iloc[ind, 0])
    three5three_list = [name for name in control_df.loc[:, 'three5three_list']]
    return select_list, three5three_list


def write_select_report(control_df, select_set, report_filename):
    """Write a csv report with the details of the selected taxa"""
    # control_df = parse_control_file(control_file)
    taxa_to_write = []
    for index in control_df.index:
        if control_df.iloc[index, 0] in select_set:
            values = control_df.iloc[index, 0:5].values
            taxa_to_write.append(','.join(value for value in values))
    with open(report_filename, 'w') as report:
        report.write('Target_name,Group,Order,Family,Species\n')
        for taxon in taxa_to_write:
            report.write(f'{taxon}\n')


def parse_target_file(target_file, select_list, three5three_list):
    """Parse the target file and return a list of SeqObjects for sequences to write"""
    seqs_to_retain = []
    current_target_seqs = SeqIO.parse(target_file, 'fasta')
    for seq in current_target_seqs:
        if re.split('[-_]', seq.name)[0] in select_list:
            # if seq.name.split('-')[0] in select_list:
            seqs_to_retain.append(seq)
        elif seq.name in three5three_list:
            seqs_to_retain.append(seq)
    return seqs_to_retain


def write_filtered_targets(seqs_to_retain, filtered_target_filename):
    """Write a new target file from a list of SeqObjects"""
    with open(filtered_target_filename, 'w') as filtered_targets:
        for seq in seqs_to_retain:
            SeqIO.write(seq, filtered_targets, 'fasta')


def parse_arguments():
    parser = argparse.ArgumentParser(description=__doc__,
                                     formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument('mega353_file', type=str, help='The mega353.fasta target file')
    parser.add_argument('select_file', type=str, help='A text file containing taxa to be selected (can be specified at '
                                                      'the levels: <Target_name>, <Group>, <Order>, <Family>, '
                                                      '<Species>. A non-redundant set of four-letter codes will be '
                                                      'used to select sequences')
    parser.add_argument("-filtered_target_file", type=str, help='File name for the filtered target set. Default is '
                                                                '"filtered_target_file.fasta"',
                        default='filtered_target_file.fasta')
    parser.add_argument("-report_filename", type=str, help='Filename for the report file. Default is '
                                                           '"new_sequences_added.csv"',
                        default='new_sequences_added.csv')
    parser.add_argument("-list_filtering_options", action='store_true',
                        help="Writes a file named 'filtering_options.csv', containing a list of codes and names that "
                             "can be used in the 'select.txt' file for filtering", default=False)
    results = parser.parse_args()
    return results


